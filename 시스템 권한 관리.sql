--권한 관리

CREATE USER ORCLSTUDY
IDENTIFIED BY ORACLE;

SELECT * FROM DBA_USERS;

GRANT RESOURCE, CREATE SESSION, CREATE TABLE TO ORCLSTUDY;


-- 권한 회수
REVOKE RESOURCE, CREATE SESSION FROM ORCLSTUDY;



-- scott 계정으로 ORCLSTUDY에 권한 주기

--scott 계정
CREATE TABLE TEMP(
    COL1 VARCHAR2( 20 ),
    COL2 VARCHAR2( 20 ));
    
DESC TEMP;

GRANT SELECT ON TEMP TO ORCLSTUDY;

GRANT INSERT ON TEMP TO ORCLSTUDY;

SELECT * FROM TEMP;

REVOKE SELECT, INSERT ON TEMP FROM ORCLSTUDY; -- 권한 회수


